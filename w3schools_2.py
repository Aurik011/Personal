# LIST- CONTINUED
# Add list items
thislist = ["apple", "banana", "cherry"]
thislist.append("orange")
print(thislist)
# The insert() method inserts an item at the specified index:
thislist = ["apple", "banana", "cherry"]
thislist.insert(1, "orange")
print(thislist)
# Extend list
thislist = ["apple", "banana", "cherry"]
tropical = ["mango", "pineapple", "papaya"]
thislist.extend(tropical)
print(thislist)


